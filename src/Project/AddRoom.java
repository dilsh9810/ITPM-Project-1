    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        String building = txtbuilding.getText().toString();
        String roomname = txtroomname.getText().toString();
        String capacity = txtcapac.getText().toString();
        String faculty = cmbbxfaculty.getSelectedItem().toString();
        String roomt = cmbbxroomtype.getSelectedItem().toString();
        
            //Validations

        if (faculty.equals("Faculty of Business") && roomt.equals("Laboratory") || faculty.equals("School of tourism") && roomt.equals("Laboratory")) {

            JOptionPane.showMessageDialog(this, "Invalid Room Type Please Select Valid type");
            System.exit(0);

        }

        if (building.equals("") && roomname.equals("") && capacity.equals("")) {

            JOptionPane.showMessageDialog(this, "Please enter all fileds");

        } else {
            
                try {
                      
                           // Insert Operation for location Management                   
                        
                           Connection c = dbcon.getConnection();
                       
                           String sql = "insert into Location (FACULTY,BUILDING,ROOMNAME,ROOMTYPE,CAPACITY) values(?,?,?,?,?);";
                       
                            pst = c.prepareStatement(sql);
                      
                            pst.setString(1, cmbbxfaculty.getSelectedItem().toString());
                            pst.setString(2, txtbuilding.getText().toString());
                            pst.setString(3, txtroomname.getText().toString());
                            pst.setString(4, cmbbxroomtype.getSelectedItem().toString());
                            pst.setString(5, txtcapac.getText().toString());

                            JOptionPane.showMessageDialog(this, "Location added successfully");

                            Managelocations mloc = new Managelocations();
                            mloc.setVisible(true);

                            pst.execute();

                            c.close();

                        } catch (Exception e) {

                                System.err.println(e.getMessage());

            }

        }
    }//GEN-LAST:event_btnSaveActionPerformed

